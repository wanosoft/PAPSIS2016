 private void muestraCodigoFactura() {
        try {
            int nuevoCodigo;
            int codigoAnterior = 0;
            rs = fventaDao.codigoFacturaTop();
            while (rs.next()) {
                codigoAnterior = rs.getInt(1);
            }
            nuevoCodigo = codigoAnterior / 1 + 1;
            lblCodigoFactura.setText(String.valueOf("000" + nuevoCodigo));
        } catch (Exception e) {
        }

    }

    private void calcularTotales() {
        try {

            total = 0;
            int filas = model.getRowCount();
            for (int i = 0; i < filas; i++) {
                total += Double.parseDouble(tblRegistro.getValueAt(i, 5).toString());
            }
            txtTotal.setText(String.format("%.2f", total));
        } catch (Exception e) {
        }
    }

    private void limpiacontroles() {
        txtCantidad.setText(null);
        txtDescripcion.setText(null);
        txtDni.setText(null);
        txtImporte.setText(null);
        txtTotal.setText(null);
        txtNombres.setText(null);
        txtNombres.grabFocus();
        int filas = tblRegistro.getRowCount();
        for (int i = 0; filas > i; i++) {
            model.removeRow(0);
        }

    }

    private void calendario() {
        Calendar cal1 = Calendar.getInstance();
        String mesInt = "";

        int mes = cal1.get(Calendar.MONTH);
        int mesActual = mes + 1;
        if (mesActual < 9) {
            mesInt = "0" + mesActual + "/";
        } else {
            mesInt = mesActual + "/";
        }

        int dia = cal1.get(Calendar.DAY_OF_MONTH);
        String diaInt = "";
        if (dia < 10) {
            diaInt = "0" + dia + "/";
        } else {
            diaInt = dia + "/";
        }
        int año = cal1.get(Calendar.YEAR);
        int añoAc = año - 2000;
        txtFecha.setText("" + diaInt + mesInt + añoAc);
    }

