    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        try {
            clienteDao clienDao = new clienteDao();
            clienteTo toCliente = new clienteTo();
            facturaDao factDao = new facturaDao();
            facturaTo toFactura = new facturaTo();

            detalleDao oDao = new detalleDao();
//registrar cliente
            toCliente.setDni(txtDni.getText());
            toCliente.setApellidos(txtNombres.getText());
            toCliente.setDireccion(txtDireccion.getText());
            clienDao.registrarClienteFactura(toCliente);

            String codCliente = factDao.obteneriIDcliente();
// grabar factura
            toFactura.setCodigoFactura(lblCodigoFactura.getText());
            toFactura.setCodigoCliente(codCliente);
            toFactura.setFecha(txtFecha.getText());
            toFactura.setIgv(Double.parseDouble(txtIGV.getText()));
            toFactura.setSubtotal(Double.parseDouble(txtSubTotal.getText()));
            toFactura.setTotal(total);
            factDao.grabarFacturaVentas(toFactura);

            String idFactura = factDao.obteneriIDfactura();
            int filas = tblRegistro.getRowCount();

            //grabar detalle de reparaciones
          for (int i = 0; i < filas; i++) {
                detalleVentaTo to = new detalleVentaTo();
                to.setCodfactura(idFactura);
                to.setCodServ(tblRegistro.getValueAt(i, 0).toString());               
                to.setPrecio(Double.parseDouble(tblRegistro.getValueAt(i, 4).toString()));
                to.setCantidad(Integer.parseInt(tblRegistro.getValueAt(i, 2).toString()));
                to.setImporte(Double.parseDouble(tblRegistro.getValueAt(i, 5).toString()));
                to.setCodempleado(tblRegistro.getValueAt(i, 1).toString());
                oDao.grabarDetalleReparaciones(to);
             
            }

            JOptionPane.showMessageDialog(rootPane, "factura realizada   C:");
            muestraCodigoFactura();
            limpiacontroles();
            System.out.println("\n Factura Realizada Fecha"+txtFecha.getText()+"importe S/"+txtImporte.getText());
            System.out.println("*******************************************************\n");
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btnGrabarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
            String  codigoEmpleado = null;
            servicioTo to = new servicioTo();
          // registra servicio
            to.setObservacion(txtDescripcion.getText());
            to.setCosto(Double.valueOf(txtImporte.getText()));
            to.setFecha(txtFecha.getText());
            servDao.RegistrarReparacion(to);
           // retorna datos del empleado _ devuelve codigo
            rs = empl.cargaEmpleado();
            {
                while (rs.next()) {
                    if (cboEmpleado.getSelectedItem().equals(rs.getString(4))) {
                        codigoEmpleado = rs.getString(1);
                    }
                }
            }
            // agrega registro a la tabla
            String codigo = servDao.obtenerCodServc();
            model = (DefaultTableModel) tblRegistro.getModel();
            double importe = Double.parseDouble(txtImporte.getText());

            if (importe > 0) {
                Object[] registros = {
                    codigo, codigoEmpleado,txtCantidad.getText(), txtDescripcion.getText(), txtImporte.getText(), txtImporte.getText()
                };
                model.addRow(registros);
                calcularTotales();
            }
            txtCantidad.setText("");
            txtImporte.setText("");
            txtDescripcion.setText("");
            txtCantidad.grabFocus();

        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

