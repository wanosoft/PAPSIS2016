    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        try {
            String mensaje = "";
            marcaProductoTo to = new marcaProductoTo();
            to.setMarca(txtMarca.getText());
            to.setModelo(txtModelo.getText());
            to.setCaracteristica(txtCaracteristicas.getText());

            if (txtMarca.getText().isEmpty() || txtModelo.getText().isEmpty()) {
                mensaje = "Sin registros... !!";
            } else {
                if (sw) {
                    daoMarca.grabar(to);
                    mensaje = "Registro Grabado Correctamente...";
                } else {
                    to.setCodigo(txtCodigo.getText());
                    daoMarca.update(to);
                    mensaje = "registro Modificado";
                }

            }

            JOptionPane.showMessageDialog(rootPane, mensaje);
            getRootPane().setDefaultButton(btnNuevo);
            habilitaControles(false);
            limpiaControles();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnGrabarActionPerformed

