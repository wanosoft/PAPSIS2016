    private void txtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyReleased
        try {
            model = (DefaultTableModel) tblRegistro.getModel();
            String marca, modelo, caracteristica;
            marca = txtBuscar.getText();
            modelo = txtBuscar.getText();
            caracteristica = txtBuscar.getText();
            rs = daoMarca.buscar(marca, modelo, caracteristica);
            while (model.getRowCount() > 0) {
                model.removeRow(0);
            }
            if (txtBuscar.getText().isEmpty()) {

            } else {
                while (rs.next()) {
                    Object[] regis = {
                        rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4)
                    };
                    model.addRow(regis);
                }
            }

        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_txtBuscarKeyReleased

  

